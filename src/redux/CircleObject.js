class CircleObject {
    constructor(id, x, y, r, stroke, fill, opacity, overlap){
        this.id = id;
        this.x = x;
        this.y = y;
        this.r = r;
        this.stroke = stroke;
        this.fill = fill;
        this.opacity = opacity;
        this.overlap = overlap;
    }
}

export default CircleObject